int8_t lampModeShift_flag = 0;//0为未转换，1为从其他模式转换为氛围模式，-1为从氛围模式转为其他模式
uint16_t cnt = 0,total_cycle = 0;
uint16_t now_time = 0;//单位：s
void timeProcess(void)
{
	//每进一次TIM14中断回调cnt就加一次,加到800说明过了1s,now_time++,cnt置零
	cnt++;
	if(cnt == 800){
		now_time++;
		cnt = 0;
	}
	//氛围模式开始，从0开始计数，cnt与now_time均置零
	if(lampModeShift_flag == 1){
		cnt = 0;
		now_time = 0;
	}
	//超过氛围模式总周期，溢出重新计数
	if(now_time > total_cycle)
		now_time = 0;
}
